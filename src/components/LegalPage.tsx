function renderInline(text: string): ReactNode[] {
  const result: ReactNode[] = [];
  const pattern = /(\*\*([^*]+)\*\*|\[([^\]]+)\]\(([^)]+)\))/g;
  let lastIndex = 0;
  let match: RegExpExecArray | null;

  while ((match = pattern.exec(text)) !== null) {
    if (match.index > lastIndex) {
      result.push(text.slice(lastIndex, match.index));
    }

    if (match[2]) {
      result.push(
        <strong key={`b-${match.index}`} className="font-bold text-slate-950">
          {match[2]}
        </strong>,
      );
    } else if (match[3] && match[4]) {
      result.push(
        <a
          key={`a-${match.index}`}
          href={match[4]}
          className="font-bold text-blue-600 underline underline-offset-4"
        >
          {match[3]}
        </a>,
      );
    }

    lastIndex = pattern.lastIndex;
  }

  if (lastIndex < text.length) {
    result.push(text.slice(lastIndex));
  }

  return result;
}

function renderBlock(block: string, index: number): ReactNode {
  const t = block.trim();
  if (!t) return null;

  if (t === '---') {
    return <hr key={index} className="my-8 border-slate-200" />;
  }

  if (t.startsWith('# ')) {
    return (
      <h1 key={index} className="mt-4 text-4xl font-black leading-tight text-slate-950">
        {t.replace(/^#\s+/, '')}
      </h1>
    );
  }

  if (t.startsWith('## ')) {
    return (
      <h2 key={index} className="mt-10 text-2xl font-black text-slate-950">
        {t.replace(/^##\s+/, '')}
      </h2>
    );
  }

  if (t.startsWith('### ')) {
    return (
      <h3 key={index} className="mt-7 text-xl font-black text-slate-950">
        {t.replace(/^###\s+/, '')}
      </h3>
    );
  }

  const lines = t.split('\n');
  if (lines.every((line) => /^\s*[-*]\s+/.test(line))) {
    return (
      <ul key={index} className="mt-4 list-disc space-y-2 pl-6 leading-8 text-slate-700">
        {lines.map((line, i) => (
          <li key={i}>{renderInline(line.trim().replace(/^[-*]\s+/, ''))}</li>
        ))}
      </ul>
    );
  }

  return (
    <p key={index} className="mt-4 whitespace-pre-line leading-8 text-slate-700">
      {renderInline(t)}
    </p>
  );
}

function LegalDocument({ text }: { text: string }) {
  return <>{text.split(/\n\s*\n/).map(renderBlock)}</>;
}

