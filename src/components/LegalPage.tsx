function RenderSections({ sections }: { sections: LegalSection[] }) {
  return (
    <>
      {sections.map((section) => (
        <section key={section.title}>
          <LegalHeading>{section.title}</LegalHeading>
          {section.paragraphs?.map((paragraph) => (
            <LegalText key={paragraph}>{paragraph}</LegalText>
          ))}
          {section.items ? (
            <LegalList>
              {section.items.map((item) => (
                <li key={item}>{item}</li>
              ))}
            </LegalList>
          ) : null}
        </section>
      ))}
    </>
  );
}

